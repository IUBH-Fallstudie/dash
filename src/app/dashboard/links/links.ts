export class Links {
  linkBild: string;
  linkURL: string;
  linkName: string;
  linkBeschreibung: string;


  constructor(linkBild: string, linkURL: string, linkName: string, linkBeschreibung: string) {
    this.linkBild = linkBild;
    this.linkURL = linkURL;
    this.linkName = linkName;
    this.linkBeschreibung = linkBeschreibung;
  }
}
